mt19937_64 rng(chrono::steady_clock::now().time_since_epoch().count());

inline int getRand(int L, int R) {
    if (L > R) swap(L, R);
    return (int)(rng() % ((uint64_t)R - L + 1) + L);
}

template <typename T> bool chmin(T &lhs, T rhs) {return lhs > rhs ? lhs = rhs, 1 : 0;}
template <typename T> bool chmax(T &lhs, T rhs) {return lhs < rhs ? lhs = rhs, 1 : 0;}

const int dx[] = {1, 0,-1, 0};
const int dy[] = {0, 1, 0,-1};

void solve() {
    int N, M; cin >> N >> M;
    
    vector<string> board(N);
    for (string &str : board) cin >> str;
    
    auto valid = [&](int x, int y) -> bool {
        return 0 <= x and x < N and 0 <= y and y < M and board[x][y] == '1';
    };
    
    auto dfs = [&](int _x, int _y, int &cnt, int &x_min, int &x_max, int &y_min, int &y_max) -> void {
        auto dfs_impl = [&](int x, int y, auto &_dfs) -> void {
            ++cnt, chmin(x_min, x), chmax(x_max, x), chmin(y_min, y), chmax(y_max, y);
            board[x][y] = '0';
            for (int d : {0, 1, 2, 3}) {
                if (valid(x+dx[d], y+dy[d])) _dfs(x+dx[d], y+dy[d], _dfs);
            }
        };
        dfs_impl(_x, _y, dfs_impl);
    };
    
    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < M; ++j) {
            if (board[i][j] == '1') {
                int cnt = 0, x_min = i, x_max = i, y_min = j, y_max = j;
                dfs(i, j, cnt, x_min, x_max, y_min, y_max);
                // debug(cnt, x_min, x_max, y_min, y_max);
                if (cnt != (x_max - x_min + 1) * (y_max - y_min + 1)) return cout << "No\n", void();
            }
        }
    }
    cout << "Yes\n";
}

int32_t main() {
    fastIO();
    
    int t = 1; cin >> t;
    for (int _ = 1; _ <= t; ++_) {
        // cout << "Case #" << _ << ": ";
        solve();
    }
    
    return 0;
}
