void solve() {
    int N, H; cin >> N >> H;
    
    vector<int> vec(N);
    for (auto &x : vec) cin >> x;
    
    int max_1 = 0, max_2 = 0;
    for (auto x : vec) {
             if (x > max_1) max_2 = max_1, max_1 = x;
        else if (x > max_2) max_2 = x;
    }
    
    int days = H / (max_1 + max_2) * 2;
    if (H % (max_1 + max_2) > 0) ++days;
    if (H % (max_1 + max_2) > max_1) ++days;
    cout << days << "\n";
}

int32_t main() {
    fastIO();
    
    int t = 1; cin >> t;
    for (int _ = 1; _ <= t; ++_) {
        // cout << "Case #" << _ << ": ";
        solve();
    }
    
    return 0;
}
