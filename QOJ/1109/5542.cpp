void solve() {
    int N; cin >> N;
    
    vector<int> A(N);
    int d = 0;
    for (int &x : A) cin >> x, d = gcd(d, x);
    
    MinHeap<int> pq;
    for (int &x : A) pq.ee(__builtin_ctz(x / d));
    while (SZ(pq) >= 2) {
        int a = pq.top(); pq.pop();
        int b = pq.top(); pq.pop();
        pq.ee(a + 1);
    }
    cout << (d << pq.top()) << "\n";
}

signed main() {
    IOS();
    int t = 1; // cin >> t;
    for (int i = 1; i <= t; ++i) solve();
    return 0;
}

